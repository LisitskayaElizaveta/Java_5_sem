public class Type4 {
    public static void main(String args[]) {
        String a = "101";
        byte byte1 = 10;
        String t1 = a + byte1;
        System.out.println(t1);

        int int1 = 123456789;
        String  t2 = a + int1;
        System.out.println(t2);

        float float1 = 1.234f;
        String  t3 = a + float1;
        System.out.println(t3);

        char char1 = 'a';
        String  t4 = a + char1;
        System.out.println(t4);
    }
}
